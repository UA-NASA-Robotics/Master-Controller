int WorldAt(int x, int y)
{
    if (x >= 0 && x < worldWidth && y >= 0 && y < worldHeight)
    {
        return world[y*worldWidth+x];
    }
    else
    {
        return -1;
    }
}
#define RockSize 95
void addRock(int _x, int _y)
{
    int x,y;
    /* Should make a square rock that is the size of the robot in the Map */
    for(y = _y - (RockSize /2); y < (RockSize/2) + _y; y++)
    {
        for(x = _x -(RockSize /2); x < (RockSize/2) +_x; x++)
        {
            addObtaclePoint(x,y);
        }
    }
}
void addObtaclePoint(int x, int y)
{
    world[y*worldWidth+x] = 1;
}
void gentateobstacleBoarder(int boarderWidth)
{
    unsigned long i;
    unsigned long worldArea = (worldHeight*worldWidth);
    // Top & Bottom Of Map boarder
    for(i = 0; i < worldWidth * boarderWidth;i++){
        world[i] = 1;
        world[worldArea - i -1] = 1;
    }
    int j;
    for(i = 0; i < worldArea;i+=worldWidth){
        for(j = 0;j < boarderWidth;j++)
        {
            world[i + j] = 1;
            world[i - (j+1)] = 1;
        }

    }
}
